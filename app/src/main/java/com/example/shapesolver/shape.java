package com.example.shapesolver;

public class shape {

    int shapeImage;
    String shapeName;

    public shape(int shapeImage, String shapeName) {
        this.shapeImage = shapeImage;
        this.shapeName = shapeName;
    }

    public int getShapeImage() {
        return shapeImage;
    }

    public String getShapeName() {
        return shapeName;
    }

}
